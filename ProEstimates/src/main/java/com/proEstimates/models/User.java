package com.proEstimates.models;

public class User {
	private int user_id;
	private String user_firstName;
	private String user_lastName;
	private int user_zipCode;
	private int user_phoneNumber;
	private String user_email;
	private String user_password;
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public String getUser_firstName() {
		return user_firstName;
	}
	public void setUser_firstName(String user_firstName) {
		this.user_firstName = user_firstName;
	}
	public String getUser_lastName() {
		return user_lastName;
	}
	public void setUser_lastName(String user_lastName) {
		this.user_lastName = user_lastName;
	}
	public int getUser_zipCode() {
		return user_zipCode;
	}
	public void setUser_zipCode(int user_zipCode) {
		this.user_zipCode = user_zipCode;
	}
	public int getUser_phoneNumber() {
		return user_phoneNumber;
	}
	public void setUser_phoneNumber(int user_phoneNumber) {
		this.user_phoneNumber = user_phoneNumber;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public String getUser_password() {
		return user_password;
	}
	public void setUser_password(String user_password) {
		this.user_password = user_password;
	}

}
